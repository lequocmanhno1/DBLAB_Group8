    private void initForm() {
        Booking booking = BookingsController.getBookingById(bookingId);
        lbHeader.setText("Booking for " + booking.getGuestLastName() + ", " + booking.getGuestFirstName());

        txtLastName.setText(booking.getGuestLastName());
        txtLastName.setEnabled(false);

        txtFirstName.setText(booking.getGuestFirstName());
        txtFirstName.setEnabled(false);

        txtPhone.setText(booking.getGuestPhone());
        txtPhone.setEnabled(false);

        txtCitizenId.setText(booking.getGuestCitizenId());
        txtCitizenId.setEnabled(false);

        txtPassport.setText(booking.getGuestPassport());
        txtPassport.setEnabled(false);
        
        txtGender.setText(booking.getGender());
        txtGender.setEnabled(false);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        txtCheckIn.setText(sdf.format(new Date(booking.getCheckInDate().getTime())));
        txtCheckIn.setEnabled(false);

        txtCheckOut.setText(sdf.format(new Date(booking.getCheckOutDate().getTime())));
        txtCheckOut.setEnabled(false);

        txtAdults.setText(String.valueOf(booking.getAdultAmount()));
        txtAdults.setEnabled(false);

        txtChildren.setText(String.valueOf(booking.getChildrenAmount()));
        txtChildren.setEnabled(false);

        Room room = RoomsController.getRoomByRoomId(booking.getRoomId());
        lbRoom.setText(room.getRoomNumber() + " at $" + room.getPricePerNight() + " per night");

        Staff staff = LoginController.getStaffByStaffId(booking.getStaffId());
        lbStaffName.setText(staff.getStaff_id() + " - " + staff.getStaffname());

        btCheckIn.setVisible(booking.getStatusId() == 1);
        btCancel.setVisible(booking.getStatusId() == 1);
        btCheckOut.setVisible(booking.getStatusId() == 2);

        Vector data = BookingsController.getServiceUseForBookingId(bookingId);
        tbServiceUse.setModel(new DefaultTableModel((Vector) data.get(0), (Vector) data.get(1)) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });

        btNewServiceUse.setVisible(booking.getStatusId() < 3);
        btDeleteServiceUse.setVisible(booking.getStatusId() < 3);
    }

    public void refreshTbService() {
        Vector data = BookingsController.getServiceUseForBookingId(bookingId);
        tbServiceUse.setModel(new DefaultTableModel((Vector) data.get(0), (Vector) data.get(1)) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
    }


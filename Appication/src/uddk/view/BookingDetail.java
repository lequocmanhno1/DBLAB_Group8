    private void btDeleteServiceUseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteServiceUseActionPerformed
        if (tbServiceUse.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Please select at least one record!", "Invalid service", 0);
        } else {
            int serviceId = Integer.parseInt(tbServiceUse.getValueAt(tbServiceUse.getSelectedRow(), 0).toString());
            int confirm = JOptionPane.showConfirmDialog(this, "Do you want to delete service use with ID: " + serviceId);
            if (confirm == 0) {
                BookingsController.deleteServiceUse(bookingId, serviceId);
                refreshTbService();
            }
        }
    }//GEN-LAST:event_btDeleteServiceUseActionPerformed


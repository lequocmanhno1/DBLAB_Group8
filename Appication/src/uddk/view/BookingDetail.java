    private void btCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCheckOutActionPerformed
        double totalRoomPrice = BookingsController.getTotalRoomPrice(bookingId);
        double totalServicePirce = BookingsController.getTotalServicePrice(bookingId);
        String content = "Room price: $" + totalRoomPrice + "\n" + "Service price: $" + totalServicePirce + "\n" + "Total: $" + (totalRoomPrice + totalServicePirce) + "\n" + "Do you want to check-out?";
        int confirm = JOptionPane.showConfirmDialog(this, content);
        if (confirm == 0) {
            BookingsController.checkOut(bookingId);
            this.parent.initTBBooking();
            this.dispose();
        }
    }//GEN-LAST:event_btCheckOutActionPerformed

